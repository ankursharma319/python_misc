
def average(a, b):
    """Compute and return the arithmetic mean of a and b"""
    return (a + b) * 0.5


def distance(a, b):
    """Returns the distance or difference between two numbers a and b"""
    return abs(a-b)


def geometric_mean(a, b):
    """Returns gemoetric mean of two numbers a and b"""
    return (a*b)**0.5


def pyramid_volume(A, h):
    """Returns the volume of a pyramid whose base area is A and height is h"""
    return A*h/3.0